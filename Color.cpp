#include "Color.h"


// ======== Return RGB Colors ========
double Color::getColorRed() {
	return red;
}

double Color::getColorGreen() {
	return green;
}

double Color::getColorBlue() {
	return blue;
}


// ========== Set RGB Colors ==========
void Color::setColorRed(double r) {
	red = r;
}

void Color::setColorGreen(double g) {
	green = g;
}

void Color::setColorBlue(double b) {
	blue = b;
}
